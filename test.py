import os
import shutil

# Directory containing your repositories
repositories_dir = '/workspaces/gen-ai-apps'

# List of files and folders to delete within each repository
files_and_folders_to_delete = ['.vscode', '.git', '.github', '.gitignore', '.idea']

# Iterate through repositories in the directory
for repo_name in os.listdir(repositories_dir):
    repo_path = os.path.join(repositories_dir, repo_name)

    # Check if it's a directory
    if os.path.isdir(repo_path):
        print(f"Processing repository: {repo_name}")
        
        # Iterate through files and folders to delete
        for item in files_and_folders_to_delete:
            item_path = os.path.join(repo_path, item)

            # Check if the item exists, and if it's a directory, delete it recursively
            if os.path.exists(item_path):
                if os.path.isdir(item_path):
                    shutil.rmtree(item_path)
                    print(f"Deleted folder: {item}")
                else:
                    os.remove(item_path)
                    print(f"Deleted file: {item}")

print("Cleanup complete.")







